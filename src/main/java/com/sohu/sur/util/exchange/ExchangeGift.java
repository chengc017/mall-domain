package com.sohu.sur.util.exchange;

import java.util.Date;

import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement(name="gift")
public class ExchangeGift {

	private long id;
	private String name;
	private String viewName;
	private long sortId;        //分类Id	
	private Integer count;      //商品数量
	private Integer stockCount; //库存数量
	private Integer marketValue;//市场价（积分）	
	private String logo;		//logo图片
	private String url;			//礼品详细信息url
	private Date shelfTime; 	//上架时间	
	private long activityid; 	//活动id
	private Integer limitPrice;
	private Integer limitCount;
	private Integer userGiftCount;//每人次兑换个数
	private Date startTime;
	private Date endTime;
	
	private String channelIds; //频道
	private String medals;     //所需勋章
	private Integer priority; 
	
	
	

	
	public ExchangeGift() {}



	public long getId() {
		return id;
	}



	public void setId(long id) {
		this.id = id;
	}



	public String getName() {
		return name;
	}



	public void setName(String name) {
		this.name = name;
	}



	public String getViewName() {
		return viewName;
	}



	public void setViewName(String viewName) {
		this.viewName = viewName;
	}



	public long getSortId() {
		return sortId;
	}



	public void setSortId(long sortId) {
		this.sortId = sortId;
	}



	public Integer getCount() {
		return count;
	}



	public void setCount(Integer count) {
		this.count = count;
	}



	public Integer getStockCount() {
		return stockCount;
	}



	public void setStockCount(Integer stockCount) {
		this.stockCount = stockCount;
	}



	public Integer getMarketValue() {
		return marketValue;
	}



	public void setMarketValue(Integer marketValue) {
		this.marketValue = marketValue;
	}



	public String getLogo() {
		return logo;
	}



	public void setLogo(String logo) {
		this.logo = logo;
	}



	public String getUrl() {
		return url;
	}



	public void setUrl(String url) {
		this.url = url;
	}



	public Date getShelfTime() {
		return shelfTime;
	}



	public void setShelfTime(Date shelfTime) {
		this.shelfTime = shelfTime;
	}



	public Long getActivityid() {
		return activityid;
	}



	public void setActivityid(Long activityid) {
		this.activityid = activityid;
	}



	public Integer getLimitPrice() {
		return limitPrice;
	}



	public void setLimitPrice(Integer limitPrice) {
		this.limitPrice = limitPrice;
	}



	public Integer getLimitCount() {
		return limitCount;
	}



	public void setLimitCount(Integer limitCount) {
		this.limitCount = limitCount;
	}



	public Integer getUserGiftCount() {
		return userGiftCount;
	}



	public void setUserGiftCount(Integer userGiftCount) {
		this.userGiftCount = userGiftCount;
	}



	public Date getStartTime() {
		return startTime;
	}



	public void setStartTime(Date startTime) {
		this.startTime = startTime;
	}



	public Date getEndTime() {
		return endTime;
	}



	public void setEndTime(Date endTime) {
		this.endTime = endTime;
	}



	public String getChannelIds() {
		return channelIds;
	}



	public void setChannelIds(String channelIds) {
		this.channelIds = channelIds;
	}



	public String getMedals() {
		return medals;
	}



	public void setMedals(String medals) {
		this.medals = medals;
	}



	public Integer getPriority() {
		return priority;
	}



	public void setPriority(Integer priority) {
		this.priority = priority;
	}
	

	

}
