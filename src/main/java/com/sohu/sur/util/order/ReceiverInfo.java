package com.sohu.sur.util.order;

public class ReceiverInfo {
		
	private Long id;
	private String dealId;//交易id（订单id）
	private String receiveUserName;
	private String receiveTel;
	private String receiveAddr;
	private String receivePostCode;
	private String descr;	
	//userAddressInfo
	
	public ReceiverInfo() {
	}
	

	
	public ReceiverInfo(Long id, String dealId, String receiveUserName,
			String receiveTel, String receiveAddr, String receivePostCode,
			String descr) {
		super();
		this.id = id;
		this.dealId = dealId;
		this.receiveUserName = receiveUserName;
		this.receiveTel = receiveTel;
		this.receiveAddr = receiveAddr;
		this.receivePostCode = receivePostCode;
		this.descr = descr;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}



	public String getDealId() {
		return dealId;
	}



	public void setDealId(String dealId) {
		this.dealId = dealId;
	}



	public String getReceiveUserName() {
		return receiveUserName;
	}
	public void setReceiveUserName(String receiveUserName) {
		this.receiveUserName = receiveUserName;
	}
	public String getReceiveTel() {
		return receiveTel;
	}
	public void setReceiveTel(String receiveTel) {
		this.receiveTel = receiveTel;
	}
	public String getReceiveAddr() {
		return receiveAddr;
	}
	public void setReceiveAddr(String receiveAddr) {
		this.receiveAddr = receiveAddr;
	}
	public String getReceivePostCode() {
		return receivePostCode;
	}
	public void setReceivePostCode(String receivePostCode) {
		this.receivePostCode = receivePostCode;
	}
	public String getDescr() {
		return descr;
	}
	public void setDescr(String descr) {
		this.descr = descr;
	}
	


}
