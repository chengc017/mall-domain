package com.sohu.sur.dto;

/**
 * 首页类型
 * @author xiayanming
 *
 */
public class LogoTypeVO {

	private String suffixName;
	private int value;
	
	private int width;
	private int height;
	
	private boolean isLiangdian;//是否是亮点图片
	
	private boolean checked;//点选按钮是否选中
	
	private String desc;

	public String getSuffixName() {
		return suffixName;
	}

	public void setSuffixName(String name) {
		this.suffixName = name;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

	public boolean getIsLiangdian() {
		return isLiangdian;
	}

	public void setIsLiangdian(boolean isLiangdian) {
		this.isLiangdian = isLiangdian;
	}

	public boolean isChecked() {
		return checked;
	}

	public void setChecked(boolean checked) {
		this.checked = checked;
	}
	
	
}
