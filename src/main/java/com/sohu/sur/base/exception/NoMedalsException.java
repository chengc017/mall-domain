package com.sohu.sur.base.exception;

public class NoMedalsException extends SucTradeException{
	
	private String status ; //状态
	/**
     * Class constructor.
     */
    public NoMedalsException() {
        super();
    }

    /**
     * Class constructor.
     *
     * @param message	java.lang.String
     */
    public NoMedalsException(String message) {
        super(message);
    }

    public NoMedalsException(String status, String message) {
        super(message);
        this.setStatus(status);
    }
    
    
    /**
     * Class constructor.
     *
     * @param message
     * @param cause
     */
    public NoMedalsException(String message, Throwable cause) {
        super(message, cause);
    }

    /**
     * Class constructor.
     *
     * @param cause
     */
    public NoMedalsException(Throwable cause) {
        super(cause);
    }

	/**
	 * @return the status
	 */
	public String getStatus() {
		return status;
	}

	/**
	 * @param status the status to set
	 */
	public void setStatus(String status) {
		this.status = status;
	}

    
    
}
