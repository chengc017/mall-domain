package com.sohu.sur.base.exception;

/**
 * 没有足够积分的异常类
 * 
 * @author xiayanming
 *
 */
public class SucNoEnoughScoreException extends SucTradeException {

	/**
     * Class constructor.
     */
    public SucNoEnoughScoreException() {
        super();
    }

    /**
     * Class constructor.
     *
     * @param message	java.lang.String
     */
    public SucNoEnoughScoreException(String message) {
        super(message);
    }

    /**
     * Class constructor.
     *
     * @param message
     * @param cause
     */
    public SucNoEnoughScoreException(String message, Throwable cause) {
        super(message, cause);
    }

    /**
     * Class constructor.
     *
     * @param cause
     */
    public SucNoEnoughScoreException(Throwable cause) {
        super(cause);
    }
}
